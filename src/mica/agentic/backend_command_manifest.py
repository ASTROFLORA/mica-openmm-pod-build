from __future__ import annotations

from typing import Any, Dict, Iterable, Literal, Mapping, Optional, Sequence

from pydantic import BaseModel, Field


ImplementedStatus = Literal["implemented", "registered_but_blocked", "deprecated", "internal_only"]

RiskTier = Literal["read_only", "read_scope_protected", "side_effecting", "mutating", "destructive"]


class BackendCommandManifestEntry(BaseModel):
    command_name: str
    description: str
    side_effects: bool = Field(default=False)
    required_scope: tuple[str, ...] = Field(default_factory=tuple)
    input_schema: Dict[str, Any] = Field(default_factory=dict)
    output_schema: Dict[str, Any] = Field(default_factory=dict)
    binding_surface: str
    backend_authority: str
    implemented_status: ImplementedStatus
    tool_aliases: tuple[str, ...] = Field(default_factory=tuple)
    # ── CK1 — Authority Freeze metadata ──
    family: str = Field(default="unassigned", description="Family group (resource, protocol, compute, artifact, mudo, agent, kb, graphrag, cea, quetzal, models)")
    owner_lab: str = Field(default="unassigned", description="Lab responsible for this command family")
    receipt_class: str = Field(default="standard", description="Receipt class: standard | gate | identity | protocol | compute")
    risk_tier: RiskTier = Field(default="read_only")
    idempotency_required: bool = Field(default=False)
    auth_required: bool = Field(default=True)
    live_proof_required: bool = Field(default=False, description="True = route-backed receipts required; False = fixture/local acceptable for dev")
    unlock_candidate: bool = Field(default=False, description="True = read/propose command that is a candidate for unlocking")
    canonical_mutation: bool = Field(default=False, description="True = mutates canonical database state")
    execute_capable: bool = Field(default=False, description="True = command executes serverless jobs/runs")
    requires_gate: bool = Field(default=False, description="True = command requires budget and policy gates before dispatch")
    protocol_step_eligible: bool = Field(default=True, description="True = command can be compiled as a protocol step under APF.")
    campaign_eligible: bool = Field(default=False, description="True = command is a natural candidate for GoG/campaign composition.")


def _entry(
    command_name: str,
    *,
    description: str,
    binding_surface: str,
    backend_authority: str,
    implemented_status: ImplementedStatus,
    side_effects: bool = False,
    required_scope: Sequence[str] = (),
    tool_aliases: Sequence[str] = (),
    input_schema: Dict[str, Any] | None = None,
    output_schema: Dict[str, Any] | None = None,
    # ── CK1 — Authority Freeze: family metadata ──
    family: str = "unassigned",
    owner_lab: str = "unassigned",
    receipt_class: str = "standard",
    risk_tier: RiskTier = "read_only",
    idempotency_required: bool = False,
    auth_required: bool = True,
    live_proof_required: bool = False,
    unlock_candidate: bool = False,
    canonical_mutation: bool = False,
    execute_capable: bool = False,
    requires_gate: bool = False,
    protocol_step_eligible: bool = True,
    campaign_eligible: bool = False,
) -> BackendCommandManifestEntry:
    return BackendCommandManifestEntry(
        command_name=command_name,
        description=description,
        side_effects=side_effects,
        required_scope=tuple(str(item).strip() for item in required_scope if str(item).strip()),
        input_schema=dict(input_schema or {}),
        output_schema=dict(output_schema or {}),
        binding_surface=binding_surface,
        backend_authority=backend_authority,
        implemented_status=implemented_status,
        tool_aliases=tuple(str(item).strip() for item in tool_aliases if str(item).strip()),
        family=family,
        owner_lab=owner_lab,
        receipt_class=receipt_class,
        risk_tier=risk_tier,
        idempotency_required=idempotency_required,
        auth_required=auth_required,
        live_proof_required=live_proof_required,
        unlock_candidate=unlock_candidate,
        canonical_mutation=canonical_mutation,
        execute_capable=execute_capable,
        requires_gate=requires_gate,
        protocol_step_eligible=protocol_step_eligible,
        campaign_eligible=campaign_eligible,
    )


