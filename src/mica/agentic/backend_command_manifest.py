def manifest_entries_by_family(family: str) -> tuple[BackendCommandManifestEntry, ...]:
    """Return all manifest entries belonging to *family*."""
    return tuple(e for e in _MANIFEST_ENTRIES if e.family == family)


def manifest_entries_by_status(status: ImplementedStatus) -> tuple[BackendCommandManifestEntry, ...]:
    """Return all manifest entries with *status*."""
    return tuple(e for e in _MANIFEST_ENTRIES if e.implemented_status == status)


def manifest_family_inventory() -> dict[str, dict[str, int]]:
    """Return a machine-readable inventory: family -> {total, implemented, blocked}."""
    inventory: dict[str, dict[str, int]] = {}
    for e in _MANIFEST_ENTRIES:
        fam = e.family
        if fam not in inventory:
            inventory[fam] = {"total": 0, "implemented": 0, "registered_but_blocked": 0}
        inventory[fam]["total"] += 1
        if e.implemented_status == "implemented":
            inventory[fam]["implemented"] += 1
        elif e.implemented_status == "registered_but_blocked":
            inventory[fam]["registered_but_blocked"] += 1
    return inventory


def manifest_authority_summary() -> dict[str, Any]:
    """Return a compact summary of manifest authority state."""
    total = len(_MANIFEST_ENTRIES)
    implemented = sum(1 for e in _MANIFEST_ENTRIES if e.implemented_status == "implemented")
    blocked = sum(1 for e in _MANIFEST_ENTRIES if e.implemented_status == "registered_but_blocked")
    return {
        "total_commands": total,
        "implemented": implemented,
        "registered_but_blocked": blocked,
        "progress_pct": round(implemented / total * 100, 1) if total else 0.0,
        "families": manifest_family_inventory(),
    }


def is_backend_command_name(name_or_alias: str) -> bool:
    canonical = canonical_backend_command_name(name_or_alias)
    return canonical in BACKEND_COMMAND_MANIFEST


def backend_command_alias_pairs() -> Mapping[str, str]:
    return dict(BACKEND_COMMAND_ALIAS_MAP)


def manifest_entries_by_tool_alias(tool_names: Iterable[str]) -> Dict[str, BackendCommandManifestEntry]:
    bound: Dict[str, BackendCommandManifestEntry] = {}
    for tool_name in tool_names:
        normalized = str(tool_name or "").strip()
        if not normalized:
            continue
        canonical = BACKEND_COMMAND_ALIAS_MAP.get(normalized)
        if not canonical:
            continue
        bound[normalized] = BACKEND_COMMAND_MANIFEST[canonical]
    return bound
