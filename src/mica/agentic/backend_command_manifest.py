BACKEND_COMMAND_MANIFEST: Dict[str, BackendCommandManifestEntry] = {
    entry.command_name: entry for entry in _MANIFEST_ENTRIES
}

BACKEND_COMMAND_ALIAS_MAP: Dict[str, str] = {
    alias: entry.command_name
    for entry in _MANIFEST_ENTRIES
    for alias in entry.tool_aliases
}


def iter_manifest_entries() -> Iterable[BackendCommandManifestEntry]:
    return tuple(BACKEND_COMMAND_MANIFEST.values())


def canonical_backend_command_name(name_or_alias: str) -> str:
    normalized = str(name_or_alias or "").strip()
    if normalized in BACKEND_COMMAND_MANIFEST:
        return normalized
    return BACKEND_COMMAND_ALIAS_MAP.get(normalized, normalized)


def get_backend_command_manifest_entry(name_or_alias: str) -> BackendCommandManifestEntry:
    canonical = canonical_backend_command_name(name_or_alias)
    try:
        return BACKEND_COMMAND_MANIFEST[canonical]
    except KeyError as exc:
        raise KeyError(f"Unknown backend command '{name_or_alias}'") from exc


